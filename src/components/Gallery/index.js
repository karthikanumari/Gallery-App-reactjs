class Gallery extends Component {
  state = {activeImgId: imagesList[0].id}

  handleClick = id => {
    this.setState({activeImgId: id})
  }

  getImage = () => {
    const {activeImgId} = this.state
    const arr = imagesList.filter(each => each.id === activeImgId)
    const imgUrl = arr[0].imageUrl
    const altText = arr[0].imageAltText
    return <img className="active-img" src={imgUrl} alt={altText} />
  }

  render() {
    return (
      <div className="bg">
        {this.getImage()}
        <div className="container">
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
          <ul>
            {imagesList.map(eachItem => (
              <ThumbnailItem
                onThumbnailClick={this.handleClick}
                key={eachItem.id}
                details={eachItem}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
